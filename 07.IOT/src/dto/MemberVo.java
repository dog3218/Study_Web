package dto;

public class MemberVo {

private String NAME     ;
private String ID       ;
private String PW       ;
private int AGE      ;
private String ADDR     ;
private String TEL      ;

public String getNAME() {
	return NAME;
}
public void setNAME(String nAME) {
	NAME = nAME;
}
public String getID() {
	return ID;
}
public void setID(String iD) {
	ID = iD;
}
public String getPW() {
	return PW;
}
public void setPW(String pW) {
	PW = pW;
}
public int getAGE() {
	return AGE;
}
public void setAGE(int aGE) {
	AGE = aGE;
}
public String getADDR() {
	return ADDR;
}
public void setADDR(String aDDR) {
	ADDR = aDDR;
}
public String getTEL() {
	return TEL;
}
public void setTEL(String tEL) {
	TEL = tEL;
}


}
